#-----File--entities-user.inc.php
      File: entities-user.inc.php
      Desc: If a file with the name of entities-user.inc.sh exists in the
          : Entities directory, is is automatically included at the
          : end of the Entities script.
          : By default, entities-user.inc.sh is symlinked to /dev/null.
          : User should change this to point at their own bash script.
          : User should not store their include script in the Entities directory.
          : This file can be used to over-ride Entities defaults and set to
          : User's commonly used defaults for global variables and functions,
          : without having to change code in the core Entities script.
#-----File--entities-user.inc.php---------------------------------------------
